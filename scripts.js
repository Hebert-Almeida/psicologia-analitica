let currentConceptId = null;
let conceptScrollTriggers = [];
const HAS_SCROLL_TRIGGER = HAS_GSAP && typeof window.ScrollTrigger !== 'undefined';

if (HAS_SCROLL_TRIGGER) {
    gsap.registerPlugin(ScrollTrigger);
}

function killConceptScrollTriggers() {
    if (!HAS_SCROLL_TRIGGER) return;
    conceptScrollTriggers.forEach((t) => t.kill());
    conceptScrollTriggers = [];
}

function initConceptView() {
    const backBtn = document.getElementById('concept-page-back');
    backBtn?.addEventListener('click', (e) => {
        e.preventDefault();
        closeConcept();
    });
}

function splitTitleIntoChars(title) {
    const inner = document.getElementById('concept-page-title-inner');
    if (!inner) return [];
    inner.innerHTML = '';
    const chars = [];
    const words = title.split(' ');
    words.forEach((word, wIdx) => {
        const wordSpan = document.createElement('span');
        wordSpan.className = 'concept-page__title-word';
        for (const ch of word) {
            const charSpan = document.createElement('span');
            charSpan.className = 'concept-page__title-char';
            charSpan.textContent = ch;
            wordSpan.appendChild(charSpan);
            chars.push(charSpan);
        }
        inner.appendChild(wordSpan);
        if (wIdx < words.length - 1) {
            inner.appendChild(document.createTextNode(' '));
        }
    });
    return chars;
}

function renderConcept(id) {
    const meta = CONCEPTS[id];
    if (!meta) return null;

    const eyebrow = document.getElementById('concept-page-eyebrow');
    const blurb = document.getElementById('concept-page-blurb');
    const lead = document.getElementById('concept-page-lead');
    const text = document.getElementById('concept-page-text');
    const list = document.getElementById('concept-page-connections-list');

    eyebrow.textContent = 'Conceito';
    const titleChars = splitTitleIntoChars(meta.label);
    blurb.textContent = meta.blurb;
    lead.textContent = meta.lead || '';

    text.innerHTML = '';
    (meta.body || []).forEach((p) => {
        const para = document.createElement('p');
        para.textContent = p;
        text.appendChild(para);
    });

    list.innerHTML = '';
    const edges = GRAPH[id] || [];
    edges.forEach(({ to, label }) => {
        const targetMeta = CONCEPTS[to];
        if (!targetMeta) return;

        const li = document.createElement('li');
        li.className = 'connection-card';

        const shine = document.createElement('span');
        shine.className = 'connection-card__shine';
        shine.setAttribute('aria-hidden', 'true');

        const rel = document.createElement('p');
        rel.className = 'connection-card__relation';
        rel.textContent = label;

        const name = document.createElement('h3');
        name.className = 'connection-card__name';
        name.textContent = targetMeta.label;

        const blurbEl = document.createElement('p');
        blurbEl.className = 'connection-card__blurb';
        blurbEl.textContent = targetMeta.blurb;

        const goBtn = document.createElement('button');
        goBtn.type = 'button';
        goBtn.className = 'connection-card__go';
        goBtn.textContent = `Ir para ${targetMeta.label} →`;
        goBtn.addEventListener('click', (e) => {
            e.preventDefault();
            openConcept(to);
        });

        li.addEventListener('mousemove', (e) => {
            const r = li.getBoundingClientRect();
            li.style.setProperty('--mx', `${e.clientX - r.left}px`);
            li.style.setProperty('--my', `${e.clientY - r.top}px`);
        });

        li.append(shine, rel, name, blurbEl, goBtn);
        list.appendChild(li);
    });

    const result = {
        page: document.getElementById('concept-page'),
        eyebrow,
        titleChars,
        blurb,
        lead,
        paragraphs: Array.from(text.querySelectorAll('p')),
        connections: Array.from(list.querySelectorAll('.connection-card'))
    };

    if (HAS_GSAP && !REDUCED_MOTION) {
        gsap.set(result.paragraphs, { autoAlpha: 0 });
        gsap.set(result.connections, { autoAlpha: 0 });
        const connHeader = document.querySelector('.connections__header');
        if (connHeader) gsap.set(connHeader, { autoAlpha: 0 });
    }

    return result;
}

function setupConceptScrollAnimations(parts) {
    if (!HAS_SCROLL_TRIGGER || REDUCED_MOTION) return;

    const view = document.getElementById('concept-view');
    if (!view) return;

    const connHeader = document.querySelector('.connections__header');
    gsap.set(parts.paragraphs, { autoAlpha: 0 });
    if (connHeader) gsap.set(connHeader, { autoAlpha: 0 });
    gsap.set(parts.connections, { autoAlpha: 0 });

    parts.paragraphs.forEach((p) => {
        const trigger = ScrollTrigger.create({
            trigger: p,
            start: 'top 85%',
            once: true,
            onEnter: () => {
                gsap.fromTo(p,
                    { autoAlpha: 0, filter: 'blur(6px)' },
                    { autoAlpha: 1, filter: 'blur(0px)', duration: 0.7, ease: 'power3.out' }
                );
            }
        });
        conceptScrollTriggers.push(trigger);
    });

    if (connHeader) {
        const trigger = ScrollTrigger.create({
            trigger: connHeader,
            start: 'top 85%',
            once: true,
            onEnter: () => {
                gsap.fromTo(connHeader,
                    { y: 30, autoAlpha: 0 },
                    { y: 0, autoAlpha: 1, duration: 0.7, ease: 'power3.out' }
                );
            }
        });
        conceptScrollTriggers.push(trigger);
    }

    parts.connections.forEach((card, i) => {
        const fromLeft = i % 2 === 0;
        const clipFrom = fromLeft
            ? 'inset(0% 100% 0% 0%)'
            : 'inset(0% 0% 0% 100%)';
        const trigger = ScrollTrigger.create({
            trigger: card,
            start: 'top 88%',
            once: true,
            onEnter: () => {
                gsap.fromTo(card,
                    {
                        clipPath: clipFrom,
                        y: 24,
                        autoAlpha: 0,
                        scale: 0.97
                    },
                    {
                        clipPath: 'inset(0% 0% 0% 0%)',
                        y: 0,
                        autoAlpha: 1,
                        scale: 1,
                        duration: 0.85,
                        ease: 'power3.out'
                    }
                );
            }
        });
        conceptScrollTriggers.push(trigger);
    });

    const hero = document.querySelector('.concept-page__hero');
    if (hero) {
        const trigger = ScrollTrigger.create({
            trigger: hero,
            start: 'top top',
            end: '+=600',
            scrub: 0.6,
            onUpdate: (self) => {
                gsap.to(hero, {
                    y: -40 * self.progress,
                    autoAlpha: 1 - self.progress * 0.35,
                    duration: 0.2,
                    overwrite: 'auto'
                });
            }
        });
        conceptScrollTriggers.push(trigger);
    }

    ScrollTrigger.refresh();
}

function animateConceptIn(parts) {
    const view = document.getElementById('concept-view');
    if (!view) return;

    killConceptScrollTriggers();

    if (!HAS_GSAP || REDUCED_MOTION) {
        view.style.opacity = '1';
        view.style.filter = 'none';
        return;
    }

    const underline = document.getElementById('concept-page-title-underline');
    const tl = gsap.timeline({
        defaults: { ease: 'power3.out' },
        onComplete: () => setupConceptScrollAnimations(parts)
    });

    tl.fromTo(view,
        { autoAlpha: 0, filter: 'blur(18px) saturate(0.6)' },
        { autoAlpha: 1, filter: 'blur(0px) saturate(1)', duration: 0.7 }
    );

    tl.fromTo(parts.eyebrow,
        { y: 16, autoAlpha: 0 },
        { y: 0, autoAlpha: 1, duration: 0.5 },
        '-=0.4'
    );

    tl.fromTo(parts.titleChars,
        { y: 80, autoAlpha: 0, rotate: 6 },
        { y: 0, autoAlpha: 1, rotate: 0, duration: 0.9, stagger: 0.035, ease: 'power4.out' },
        '-=0.35'
    );

    if (underline) {
        tl.fromTo(underline,
            { width: 0, autoAlpha: 0 },
            { width: '100%', autoAlpha: 1, duration: 0.9, ease: 'power3.inOut' },
            '-=0.4'
        );
    }

    tl.fromTo([parts.blurb, parts.lead],
        { y: 18, autoAlpha: 0 },
        { y: 0, autoAlpha: 1, duration: 0.55, stagger: 0.1 },
        '-=0.55'
    );
}

function animateConceptSwap(parts) {
    const view = document.getElementById('concept-view');
    if (!view) return;

    killConceptScrollTriggers();

    if (!HAS_GSAP || REDUCED_MOTION) return;

    const underline = document.getElementById('concept-page-title-underline');
    const tl = gsap.timeline({
        defaults: { ease: 'power3.out' },
        onComplete: () => setupConceptScrollAnimations(parts)
    });

    tl.to(view, { filter: 'blur(0px) saturate(1)', duration: 0.05 })
        .fromTo(parts.titleChars,
            { y: 70, autoAlpha: 0, rotate: 4 },
            { y: 0, autoAlpha: 1, rotate: 0, duration: 0.75, stagger: 0.028, ease: 'power4.out' },
            0
        );

    if (underline) {
        tl.fromTo(underline,
            { width: 0, autoAlpha: 0 },
            { width: '100%', autoAlpha: 1, duration: 0.8, ease: 'power3.inOut' },
            0.1
        );
    }

    tl.fromTo([parts.eyebrow, parts.blurb, parts.lead],
        { y: 16, autoAlpha: 0 },
        { y: 0, autoAlpha: 1, duration: 0.5, stagger: 0.08 },
        0.05
    );
}

function fadeOutCurrentConceptThen(callback) {
    const view = document.getElementById('concept-view');
    if (!view || !HAS_GSAP || REDUCED_MOTION) {
        callback?.();
        return;
    }
    gsap.to(view, {
        autoAlpha: 0,
        filter: 'blur(16px) saturate(0.5)',
        duration: 0.4,
        ease: 'power2.in',
        onComplete: callback
    });
}

function openConcept(id) {
    if (!CONCEPTS[id]) return;

    const view = document.getElementById('concept-view');
    if (!view) return;

    const isAlreadyOpen = !view.classList.contains('hidden');

    if (isAlreadyOpen && currentConceptId !== id) {
        if (HAS_GSAP && !REDUCED_MOTION) {
            gsap.to(view, {
                filter: 'blur(14px) saturate(0.55)',
                autoAlpha: 0.25,
                duration: 0.3,
                ease: 'power2.in',
                onComplete: () => {
                    currentConceptId = id;
                    const parts = renderConcept(id);
                    gsap.set(view, { autoAlpha: 1 });
                    const hero = document.querySelector('.concept-page__hero');
                    if (hero) gsap.set(hero, { clearProps: 'y,opacity,autoAlpha' });
                    window.scrollTo({ top: 0, behavior: 'instant' });
                    if (parts) animateConceptSwap(parts);
                }
            });
        } else {
            currentConceptId = id;
            renderConcept(id);
            view.scrollTo?.({ top: 0 });
        }
        return;
    }

    currentConceptId = id;
    const parts = renderConcept(id);
    showOnly('concept-view');

    if (HAS_GSAP && !REDUCED_MOTION) {
        gsap.set(view, { autoAlpha: 0 });
    }
    requestAnimationFrame(() => {
        window.scrollTo({ top: 0, behavior: 'instant' });
        if (parts) animateConceptIn(parts);
    });
}

function closeConcept() {
    const view = document.getElementById('concept-view');
    if (!view) return;

    fadeOutCurrentConceptThen(() => {
        killConceptScrollTriggers();
        currentConceptId = null;
        navigateTo('map--container');
        const hero = document.querySelector('.concept-page__hero');
        if (HAS_GSAP) {
            gsap.set(view, { clearProps: 'all' });
            if (hero) gsap.set(hero, { clearProps: 'all' });
        }
    });
}
