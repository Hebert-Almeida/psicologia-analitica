/* ----------------------------- D3 mind map ---------------------------- */

function initMindMap() {
    const svgEl = document.getElementById('mindmap');
    if (!svgEl || typeof d3 === 'undefined') return;

    const nodes = Object.keys(CONCEPTS).map((id) => ({
        id,
        label: CONCEPTS[id].label,
        blurb: CONCEPTS[id].blurb
    }));

    const seen = new Set();
    const links = [];
    Object.entries(GRAPH).forEach(([source, edges]) => {
        edges.forEach(({ to: target }) => {
            const key = [source, target].sort().join('::');
            if (seen.has(key)) return;
            seen.add(key);
            links.push({ source, target });
        });
    });

    const svg = d3.select(svgEl);
    const stage = svgEl.parentNode;

    const getSize = () => ({
        width: stage.clientWidth || window.innerWidth,
        height: stage.clientHeight || window.innerHeight
    });

    let { width, height } = getSize();
    svg.attr('viewBox', `0 0 ${width} ${height}`);

    const root = svg.append('g').attr('class', 'mindmap-root');
    const linkLayer = root.append('g').attr('class', 'links');
    const nodeLayer = root.append('g').attr('class', 'nodes');

    const zoom = d3.zoom()
        .scaleExtent([0.4, 3])
        .on('zoom', (event) => {
            root.attr('transform', event.transform);
        });

    svg.call(zoom).on('dblclick.zoom', null);

    document.getElementById('zoom-in-btn')?.addEventListener('click', () => {
        svg.transition().duration(250).call(zoom.scaleBy, 1.25);
    });
    document.getElementById('zoom-out-btn')?.addEventListener('click', () => {
        svg.transition().duration(250).call(zoom.scaleBy, 0.8);
    });
    document.getElementById('zoom-reset-btn')?.addEventListener('click', () => {
        svg.transition().duration(350).call(zoom.transform, d3.zoomIdentity);
    });

    const linkSel = linkLayer.selectAll('line')
        .data(links)
        .join('line')
        .attr('class', 'mindmap-link');

    const nodeSel = nodeLayer.selectAll('g')
        .data(nodes, (d) => d.id)
        .join('g')
        .attr('class', 'mindmap-node')
        .attr('tabindex', 0)
        .attr('role', 'button')
        .attr('aria-label', (d) => `${d.label}, abrir conexões`)
        .on('click', (event, d) => {
            event.stopPropagation();
            selectNode(d);
        })
        .on('keydown', (event, d) => {
            if (event.key === 'Enter' || event.key === ' ') {
                event.preventDefault();
                selectNode(d);
            }
        })
        .call(d3.drag()
            .on('start', (event, d) => {
                if (!event.active) simulation.alphaTarget(0.25).restart();
                d.fx = d.x;
                d.fy = d.y;
            })
            .on('drag', (event, d) => {
                d.fx = event.x;
                d.fy = event.y;
            })
            .on('end', (event, d) => {
                if (!event.active) simulation.alphaTarget(0);
                d.fx = null;
                d.fy = null;
            }));

    nodeSel.append('circle')
        .attr('r', 26)
        .attr('class', 'mindmap-node__circle');

    nodeSel.append('text')
        .attr('class', 'mindmap-node__label')
        .attr('text-anchor', 'middle')
        .attr('dy', 44)
        .text((d) => d.label);

    svg.on('click', () => clearSelection());

    const simulation = d3.forceSimulation(nodes)
        .force('link', d3.forceLink(links).id((d) => d.id).distance(140).strength(0.6))
        .force('charge', d3.forceManyBody().strength(-420))
        .force('collide', d3.forceCollide().radius(60))
        .force('center', d3.forceCenter(width / 2, height / 2))
        .force('x', d3.forceX(width / 2).strength(0.04))
        .force('y', d3.forceY(height / 2).strength(0.04))
        .on('tick', () => {
            linkSel
                .attr('x1', (d) => d.source.x)
                .attr('y1', (d) => d.source.y)
                .attr('x2', (d) => d.target.x)
                .attr('y2', (d) => d.target.y);
            nodeSel.attr('transform', (d) => `translate(${d.x},${d.y})`);
        });

    const onResize = () => {
        const next = getSize();
        if (!next.width || !next.height) return;
        width = next.width;
        height = next.height;
        svg.attr('viewBox', `0 0 ${width} ${height}`);
        simulation
            .force('center', d3.forceCenter(width / 2, height / 2))
            .force('x', d3.forceX(width / 2).strength(0.04))
            .force('y', d3.forceY(height / 2).strength(0.04))
            .alpha(0.4)
            .restart();
    };
    window.addEventListener('resize', onResize);
    window.addEventListener('mindmap:shown', onResize);

    /* --------------------- Seleção / destaque / painel ------------------- */

    const panel = document.getElementById('concept-panel');
    const panelTitle = document.getElementById('concept-panel-title');
    const panelBlurb = document.getElementById('concept-panel-blurb');
    const panelLinks = document.getElementById('concept-panel-links');
    const panelOpen = document.getElementById('concept-panel-open');
    const panelClose = document.getElementById('concept-panel-close');
    const hint = document.getElementById('map-hint');

    function selectNode(d) {
        const neighbourIds = NEIGHBOURS[d.id] || new Set();
        const lit = new Set([d.id, ...neighbourIds]);

        nodeSel.classed('is-active', (n) => n.id === d.id);
        nodeSel.classed('is-neighbour', (n) => n.id !== d.id && lit.has(n.id));
        nodeSel.classed('is-dim', (n) => !lit.has(n.id));

        linkSel.classed('is-active', (l) => {
            const s = typeof l.source === 'object' ? l.source.id : l.source;
            const t = typeof l.target === 'object' ? l.target.id : l.target;
            return s === d.id || t === d.id;
        });
        linkSel.classed('is-dim', (l) => {
            const s = typeof l.source === 'object' ? l.source.id : l.source;
            const t = typeof l.target === 'object' ? l.target.id : l.target;
            return s !== d.id && t !== d.id;
        });

        panelTitle.textContent = d.label;
        panelBlurb.textContent = d.blurb;
        panelLinks.innerHTML = '';
        Array.from(neighbourIds).forEach((neighbourId) => {
            const meta = CONCEPTS[neighbourId];
            if (!meta) return;
            const chip = document.createElement('button');
            chip.type = 'button';
            chip.className = 'concept-chip';
            chip.textContent = meta.label;
            chip.addEventListener('click', (e) => {
                e.stopPropagation();
                const target = nodes.find((n) => n.id === neighbourId);
                if (target) selectNode(target);
            });
            panelLinks.appendChild(chip);
        });

        panelOpen.onclick = (e) => {
            e.preventDefault();
            openConcept(d.id);
        };

        panel.classList.remove('hidden');
        hint?.classList.add('is-faded');
    }

    function clearSelection() {
        nodeSel.classed('is-active', false).classed('is-neighbour', false).classed('is-dim', false);
        linkSel.classed('is-active', false).classed('is-dim', false);
        panel.classList.add('hidden');
        hint?.classList.remove('is-faded');
    }

    panelClose?.addEventListener('click', (e) => {
        e.stopPropagation();
        clearSelection();
    });
    panel?.addEventListener('click', (e) => e.stopPropagation());
}

