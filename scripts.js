function showOnly(targetId) {
    document.querySelectorAll('main > .container').forEach((c) => {
        if (c.id !== targetId) c.classList.add('hidden');
    });
    const target = document.getElementById(targetId);
    if (target) target.classList.remove('hidden');
    return target;
}

function navigateTo(targetId) {
    const target = showOnly(targetId);
    if (!target) return;

    if (targetId === 'map--container') {
        window.dispatchEvent(new CustomEvent('mindmap:shown'));
    }

    requestAnimationFrame(() => {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
    });
}

